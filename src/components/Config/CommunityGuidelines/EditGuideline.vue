<template>
  <div class="text-center">
    <v-dialog v-model="dialog" persistent scrollable width="500">
      <template v-slot:activator="{ on }">
        <v-btn icon elevation="0" class="mx-1" v-on="on">
          <v-icon>mdi-lead-pencil</v-icon>
        </v-btn>
      </template>
      <v-card v-if="dialog" class>
        <v-card-title class="google-font" primary-title dark>
          Update Guideline
        </v-card-title>
        <v-card-text class="px-5">
          <v-container fluid>
            <v-row class="pa-0">
              <v-col cols="12" class="pa-1 ma-0">
                <v-text-field
                  v-model="updatedData.name"
                  class="ma-0"
                  label="Name"
                  outlined
                ></v-text-field>
              </v-col>
              <v-col cols="12" class="pa-1 ma-0">
                <v-textarea
                  outlined
                  v-model="updatedData.des"
                  label="Description"
                ></v-textarea>
              </v-col>
            </v-row>
          </v-container>
        </v-card-text>

        <v-divider></v-divider>

        <v-card-actions class="grey lighten-4">
          <div class="flex-grow-1"></div>
          <v-btn color="primary" text @click="dialog = false">Close</v-btn>
          <v-btn
            color="primary"
            dark
            depressed
            @click="addGuid"
            :loading="loading"
            >Edit</v-btn
          >
        </v-card-actions>
      </v-card>
    </v-dialog>
  </div>
</template>

<script>
export default {
  name:"EditGuidelinesConfig",
  props: ["item"],
  data: () => ({
    dialog: false,
    loading: false,
    name: "",
    des: "",
    updatedData: null
  }),
  watch: {
    dialog: {
      handler() {
        if (this.dialog) {
          // if edit
          if (this.item && Object.keys(this.item).length) {
            this.updatedData = this.item;
          }
        }
      },
    },
  },
  methods: {
    addGuid() {
      this.dialog = false;
    },
  },
};
</script>

